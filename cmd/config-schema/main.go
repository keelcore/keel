func main() {
	if len(os.Args) > 1 && os.Args[1] == "--fields" {
		extractAndPrintFields(os.Stdin)
		return
	}

	root := buildObjectSchema(reflect.TypeOf(config.Config{}), "")
	schema := map[string]interface{}{
		"$schema":              "https://json-schema.org/draft/2019-09/schema",
		"title":                "Keel Configuration",
		"description":          "Generated by cmd/config-schema/main.go \u2014 do not edit manually. Regenerate: scripts/release/gen-schema.sh",
		"type":                 "object",
		"additionalProperties": false,
		"properties":           root["properties"],
	}

	out, err := yaml.Marshal(schema)
	if err != nil {
		fmt.Fprintf(os.Stderr, "yaml.Marshal: %v\n", err)
		os.Exit(1)
	}

	fmt.Println("# Generated by cmd/config-schema/main.go \u2014 do not edit manually.")
	fmt.Println("# Regenerate: scripts/release/gen-schema.sh")
	fmt.Print(string(out))
}

// buildSchema dispatches on the kind of t and returns the JSON Schema map for
// that type. path is the dotted field path used to look up fieldConstraints.
func buildSchema(t reflect.Type, path string) map[string]interface{} {
	// Dereference pointer types.
	for t.Kind() == reflect.Ptr {
		t = t.Elem()
	}

	var schema map[string]interface{}

	switch t.Kind() {
	case reflect.Struct:
		if isLeafStruct(t) {
			schema = map[string]interface{}{
				"type":        "string",
				"description": `Go duration string (e.g. "5s", "1m30s", "500ms")`,
			}
		} else {
			schema = buildObjectSchema(t, path)
		}
	case reflect.Bool:
		schema = map[string]interface{}{"type": "boolean"}
	case reflect.String:
		schema = map[string]interface{}{"type": "string"}
	case reflect.Int, reflect.Int8, reflect.Int16, reflect.Int32, reflect.Int64:
		schema = map[string]interface{}{"type": "integer"}
	case reflect.Float32, reflect.Float64:
		schema = map[string]interface{}{"type": "number"}
	case reflect.Slice:
		elem := t.Elem()
		for elem.Kind() == reflect.Ptr {
			elem = elem.Elem()
		}
		if elem.Kind() == reflect.String {
			schema = map[string]interface{}{
				"type":  "array",
				"items": map[string]interface{}{"type": "string"},
			}
		} else {
			schema = map[string]interface{}{"type": "array"}
		}
	default:
		schema = map[string]interface{}{}
	}

	// Merge any field-specific constraints.
	if constraints, ok := fieldConstraints[path]; ok {
		for k, v := range constraints {
			schema[k] = v
		}
	}

	return schema
}

// buildObjectSchema returns a JSON Schema object node for the struct type t.
func buildObjectSchema(t reflect.Type, path string) map[string]interface{} {
	// Dereference pointer types.
	for t.Kind() == reflect.Ptr {
		t = t.Elem()
	}

	props := map[string]interface{}{}
	for i := 0; i < t.NumField(); i++ {
		f := t.Field(i)
		tag := f.Tag.Get("yaml")
		if tag == "" || tag == "-" {
			continue
		}
		name := strings.SplitN(tag, ",", 2)[0]
		if name == "" || name == "-" {
			continue
		}

		var fieldPath string
		if path == "" {
			fieldPath = name
		} else {
			fieldPath = path + "." + name
		}

		props[name] = buildSchema(f.Type, fieldPath)
	}

	return map[string]interface{}{
		"type":                 "object",
		"additionalProperties": false,
		"properties":           props,
	}
}

// isLeafStruct reports whether t should be treated as a scalar leaf (e.g.
// config.Duration) rather than recursed into as an object.
func isLeafStruct(t reflect.Type) bool {
	if t.Implements(yamlUnmarshalerType) {
		return true
	}
	if reflect.PointerTo(t).Implements(yamlUnmarshalerType) {
		return true
	}
	return false
}

// extractAndPrintFields reads a JSON Schema YAML from r, walks all leaf
// property paths, and prints them sorted one per line.
func extractAndPrintFields(r io.Reader) {
	var raw map[string]interface{}
	dec := yaml.NewDecoder(r)
	if err := dec.Decode(&raw); err != nil {
		fmt.Fprintf(os.Stderr, "decode: %v\n", err)
		os.Exit(1)
	}

	fields := walkProperties(raw, "")
	sort.Strings(fields)
	for _, f := range fields {
		fmt.Println(f)
	}
}

// walkProperties recursively collects dotted leaf paths from a JSON Schema map.
func walkProperties(schema map[string]interface{}, prefix string) []string {
	raw, ok := schema["properties"]
	if !ok {
		return nil
	}
	props, ok := raw.(map[string]interface{})
	if !ok {
		return nil
	}

	var result []string
	for name, val := range props {
		var dotted string
		if prefix == "" {
			dotted = name
		} else {
			dotted = prefix + "." + name
		}

		child, ok := val.(map[string]interface{})
		if !ok {
			result = append(result, dotted)
			continue
		}

		// If the child has its own "properties", it is a nested object — recurse.
		if _, hasProps := child["properties"]; hasProps {
			result = append(result, walkProperties(child, dotted)...)
		} else {
			result = append(result, dotted)
		}
	}
	return result
}
