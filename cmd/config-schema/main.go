// cmd/config-schema/main.go
// Emits a JSON Schema (draft-07, expressed in YAML) for config.Config.
//
// Modes:
//   - No args: emit JSON Schema YAML to stdout.
//   - --fields: read JSON Schema YAML from stdin, print sorted dotted leaf paths.
//
// Generated output is committed as pkg/config/schema.yaml.
// Regenerate: scripts/release/gen-schema.sh
package main

import (
	"fmt"
	"io"
	"os"
	"reflect"
	"sort"
	"strings"

	"gopkg.in/yaml.v3"

	"github.com/keelcore/keel/pkg/config"
)

var yamlUnmarshalerType = reflect.TypeOf((*yaml.Unmarshaler)(nil)).Elem()

